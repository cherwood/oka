        <h3>Les différentes méthodes de préparation du café</h3>
        <p class="introTitle">Il existe différentes façon de préparer son café, chacune donnera un gout particulier à la boisson. A vous de choisir laquelle vous souhaitez adopter.</p>
        
        <ul class="torreUL">
            <li>
              <img src="images/infusion.jpg" alt="Infusion"/>
              <h4>L’infusion</h4>
              <p>L’infusion quand à elle requiert l'usage d'une cafetière à piston. Dans un récipient en verre, un filtre sous la forme d'un piston permet la séparation du marc de la boisson en l'isolant au fond du récipient. C'est ainsi que les professionnels goûtent le café à partir d'une mouture grossière dans une plantation ou une usine de torréfaction.
              </p>
            </li>
    
            <li>
              <img src="images/filtre-coffee.jpg" alt="Lixiviation"/>
              <h4>La lixiviation</h4>
              <p>La lixiviation est la méthode que l'on utilise couramment avec les cafetières électriques modernes. Le café filtre est préparé en faisant passer lentement de l'eau bouillante dans un filtre rempli de café moulu, donnant un lixiviat. De nos jours, les filtres jetables sont généralement en papier. Il existe aussi des filtres permanents, lavables, en nylon.
              </p>
            </li>
          </ul>
          
          <ul class="torreUL torreSmall">
            <li>
              <img src="images/perci.jpg" alt="Percolation"/>
              <h4>La percolation</h4>
              <p>La percolation est utilisé par les cafetières italiennes. Une lixiviation à vapeur forcée. Une  cafetière est constitué de deux compartiments séparés par un porte-filtre qui contient une dose de café. En chauffant, une partie de l'eau placée dans la cuve en vase clos s'évapore, ce qui produit de la vapeur qui pressurise la cuve : l'eau encore liquide remonte par le tuyau du filtre, traverse le café pour retomber finalement dans la verseuse.
              </p>
            </li>
            <li>
              <img src="images/perco-hautepression.jpg" alt="Percolation haute pression"/>
              <h4>La percolation haute pression</h4>
              <p> La percolation sous haute pression elle est un autre procédé de lixiviation à vapeur forcée qui permet de réaliser un expresso (de l'italien espresso, extrait par pression ; ne pas confondre avec « express », c'est-à-dire « très vite »). La différence avec la percolation normale vient de la pression qui est établie au moyen d'un système de pompage. Elle ainsi permet une  préparation rapide du café.
              </p>
            </li>
            <li>
              <img src="images/decoction.jpg" alt="Décoction"/>
              <h4>La décoction</h4>
              <p>La décoction est la méthode la plus ancienne de préparation du café. On utilise cette méthode dans la préparation du café turc (ou café oriental, café grec, selon les pays). Une mouture extra-fine de café mélangée à de l'eau (environ 3 cuillerées de café pour 300 ml d'eau) est portée à ébullition dans une cafetière ou tout autre pot allant sur le feu.
              </p>
            </li>
          
          </ul>
        
