        <h3>Protéger les terres et les cours d’eaux</h3>
        <p>Il y a plusieurs décennies, les plantations de café étaient visuellement impossibles à distinguer des forêts alentours. Les méthodes traditionnelles de culture du café dépendaient de l’ombre de la canopée qui abritait la faune locale ainsi que les oiseaux migrateurs et permettait une meilleure qualité de café. Dans les années 1970, l’introduction d’une nouvelle plante de café hybride nécessitant des produits agrochimiques et une culture en plein soleil mena de nombreux producteurs à déforester et à abandonner leurs méthodes traditionnelles. Cette approche high-tech des cultures ont dévasté les terres dans les pays tropicaux.</p>
        
        <p>Sur les plantations certifiées Rainforest Alliance, le café pousse en harmonie avec la nature : les sols sont sains, les cours d’eaux sont protégés, les déchets sont réduits ou recyclés, la faune sauvage et les habitats des oiseaux migrateurs prospèrent. De plus, des centaines de plantations avec lesquelles nous travaillons ont adopté des techniques intelligentes face au climat qui permettent de séquestrer le carbone. Mais encore plus important, les communautés des plantations découvrent l’importance de la protection de leurs ressources naturelles et elles acquièrent les outils et les moyens de le faire.</p>
      
