        <img src="images/revenu.jpg" alt="Revenus"/>
        <p>Alors que le marché mondiale du café est estimée à 100 milliards de dollars USD par an, la grande majorité des producteurs de café ne gagne que de faibles revenus, car ils obtiennent souvent des prix bas de leur café. Ils ont peu de choix et de nombreux producteurs finissent par abandonner leurs terres ou par détruire leurs forêts et avec les habitats naturels de la faune sauvage pour faire de la monoculture. La certification Rainforest Alliance renverse ce cycle destructeur. En effet, des études indépendantes démontrent que les producteurs qui utilisent ces méthodes durables augmentent leurs rendements et réalisent des économies grâce à une gestion plus efficace des plantations. Grâce à la certification, les producteurs ont également accès à de nouveaux marchés où ils peuvent négocier de meilleurs prix, améliorer leur accès aux concours bancaires et obtenir des primes de leur café qu’ils peuvent utiliser pour investir dans un futur plus sécurisé sur le plan économique.</p>
                      
